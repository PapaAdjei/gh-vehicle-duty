/**
 * Regenerates api/worker.bundled.js from src/duty.js + api/worker.js.
 * Run: npm run api:bundle
 */
import { readFileSync, writeFileSync } from "node:fs";

const lib = readFileSync(new URL("../src/duty.js", import.meta.url), "utf8");
const worker = readFileSync(new URL("./worker.js", import.meta.url), "utf8");

const workerBody = worker.replace(/import\s*\{[^}]*\}\s*from\s*"\.\.\/src\/duty\.js";\s*/, "");
const libBody = lib
  .replace(/export const /g, "const ")
  .replace(/export function /g, "function ")
  .replace(/export default \{[^}]*\};\s*/, "");

const out = `/**
 * gh-vehicle-duty — single-file HTTP API for Cloudflare Workers.
 *
 * BUNDLED BUILD: library + API in one file, so it can be pasted directly into
 * the Cloudflare dashboard editor (no CLI, no npm, no build step).
 *
 * Source of truth is the repo: src/duty.js + api/worker.js.
 * Do not edit this file by hand — regenerate it with \`npm run api:bundle\`.
 *
 * @license MIT
 */

/* ===== library (src/duty.js) ===== */

${libBody.trim()}

/* ===== API (api/worker.js) ===== */

${workerBody.trim()}
`;

writeFileSync(new URL("./worker.bundled.js", import.meta.url), out);
console.log(`Bundled ${out.length} chars → api/worker.bundled.js`);
